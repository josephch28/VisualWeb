    <h1>Sobre Nuestra Universidad</h1>
    
    <p>La <strong>Universidad Técnica de Ambato (UTA)</strong> es una institución de educación superior pública, acreditada y reconocida por su excelencia académica. Desde su fundación, ha sido un pilar fundamental en el desarrollo educativo, científico y cultural de la región central del país.</p>
    
    <h2>Nuestra Historia</h2>
    <p>Fundada el 18 de abril de 1969, la UTA nació con el objetivo de satisfacer las necesidades de formación profesional de la juventud ecuatoriana. A lo largo de las décadas, ha crecido exponencialmente, ampliando su oferta académica y fortaleciendo sus capacidades de investigación.</p>

